


# To see the target of a symbolic link in the Linux terminal, 
# you can use the ls command with the -l (long format) option. Here's how you can do it:


ls -l <path_to_symbolic_link>

# In your case, if you want to see where the "words" symbolic link points to, 
# you can use the following command:


ls -l words

# This will display the long format output, 
# including the target of the symbolic link. 
# The target file or directory will be displayed after the arrow (->) symbol.